    private void gravabanco(){
        String sql = "INSERT INTO tb01_senha VALUES ('" + seq + "', '" + tipo + "', 'N', NOW(), NOW());";   
        
         try{
            objbd.conectar();
            objbd.runSQL(sql);
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
    private void pegaSenha(String tipo){
        String sql = "Select max(tb01_num) from tb01_senha ";
               sql += " where id01_tipo = '" + tipo + "'";
            
        try{

        seq = 0;
        int minSenha = 0;
        
                        
        objbd.conectar();

        ResultSet rs = statement.executeQuery(sql);  
       
        if (rs.next()) 
        {
            minSenha = rs.getInt(1);
        }
        
        seq = minSenha + 1; 
        
        objbd.desconectar();
        }
        catch (Exception e){
            System.out.println(e.getMessage());
        }
    }
     
